1. What are the three levels of schema?

  * Conceptual

  * Logical

  * Physical

2. What is a conceptual schema?

  A high abstraction level design dealing with relationships between entities.

3. What is a physical schema?

  An implementation-specific design focused on actual tables/rows/relationships.

4. What are the three types of relationships that can be shown in a database
diagram?

  Depending on the multiplicity of connections among the two entities:

  * one-to-one

  * one-to-many

  * many-to-many
