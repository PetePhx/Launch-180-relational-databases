-- DML/DDL/DCL Part 1
--
-- SQL consists of 3 different sublanguages. For example, one of these
-- sublanguages is called the Data Control Language (DCL) component of SQL. It is
-- used to control access to a database; it is responsible for defining the
-- rights and roles granted to individual users. Common SQL DCL statements
-- include:

GRANT
REVOKE

-- Name and define the remaining 2 sublanguages, and give at least 2 examples of
-- each.

Solution:

  DDL

    Data Definition Language concerns with creating and altering of databases and
    tables schema which define data structures. examples:

    CREATE TABLE ...;
    ALTER TABLE ...;
    DROP TABLE ... ;



  DML

    Data Manipulation Language deals with inserting, querying, updating and
    removing of the actual data (rows) in the database tables. examples:

    INSERT INTO ... VALUES ...;
    SELECT ... FROM ...;
    UPDATE ... WHERE ...;
